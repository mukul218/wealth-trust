    <script>
        (function () {
            'use strict';

            $("#careerForm").on("submit", function (e) {
                e.preventDefault();

                var form = this;
                if (!form.checkValidity()) {
                    e.stopPropagation();
                    $(form).addClass("was-validated");
                    return;
                }

                var formData = new FormData(form);
                var $btn = $("#careerSubmitBtn");

                // Reset alert
                $("#careerAlert").html("").removeClass("alert alert-success alert-danger");

                // Change button to loader
                $btn.prop("disabled", true).html(`
            <span class="spinner-border spinner-border-sm" role="status" aria-hidden="true"></span>
            Submitting...
        `);

                $.ajax({
                    url: "./api/careers/apply.php",
                    type: "POST",
                    data: formData,
                    contentType: false,
                    processData: false,
                    success: function (res) {
                        if (res.status === "success") {
                            $("#careerAlert")
                                .addClass("alert alert-success")
                                .html(res.data.message);

                            form.reset();
                            $(form).removeClass("was-validated");
                        } else {
                            $("#careerAlert")
                                .addClass("alert alert-danger")
                                .html(res.data?.message || "Something went wrong!");
                        }
                    },
                    error: function (xhr) {
                        let res = {};
                        try {
                            res = JSON.parse(xhr.responseText);
                        } catch (e) { }
                        $("#careerAlert")
                            .addClass("alert alert-danger")
                            .html(res.data?.message || res.message || "Something went wrong!");
                    },
                    complete: function () {
                        // Restore button
                        $btn.prop("disabled", false).html("Submit");
                    }
                });
            });
        })();
    </script>


